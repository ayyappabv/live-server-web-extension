//to print object keys and values
const student = {
    name : "John",
    age : 23,
    fees : undefined,
    wasAbsentOn : [2, 5, 7, 12],
}

console.log("  ");
console.log("will print object keys");
console.log(Object.keys(student)); //will print Object keys like name, age, fees, wasAbsentOn


console.log("  ");
console.log("will print object values");
console.log(Object.values(student)); //will print Object values


